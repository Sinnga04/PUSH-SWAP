#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)

{
	int		i;
	char	c[11];

	i = 0;
	if (n == 0)
		write(fd, "0", 1);
	else if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(fd, "-", 1);
		}
		while (n > 0)
		{
			c[i++] = (n % 10) + 48;
			n /= 10;
		}
		while (i > 0)
			write(fd, &c[--i], 1);
	}
}
